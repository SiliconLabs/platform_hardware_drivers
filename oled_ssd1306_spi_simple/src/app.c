/***************************************************************************//**
 * Initialize application.
 ******************************************************************************/
void app_init(void)
{
  /* Initialize the oled. */
  oled_app_init();
}

/***************************************************************************//**
 * App ticking function.
 ******************************************************************************/
void app_process_action(void)
{
  uint8_t i;
  if(mode_display == DISPLAY_PICTURE)
  {
    glib_draw_bmp(&glib_context, oledbw_img);
    sl_udelay_wait(500000);
    glib_invert_color();
    sl_udelay_wait(500000);
    glib_normal_color();
    sl_udelay_wait(500000);
    glib_invert_color();
    sl_udelay_wait(500000);
    glib_normal_color();

    for (i = 0xFF; i > 0x00; i--)
    {
        glib_set_contrast(i);
        sl_udelay_wait(7000);
    }

    for (i = 0x00; i < 0xFF; i++)
    {
        glib_set_contrast(i);
        sl_udelay_wait(7000);
    }

    glib_scroll_right(0x00, 0x05);
    sl_udelay_wait(1000000);
    glib_stop_scroll();
    glib_draw_bmp(&glib_context, oledbw_img);

    glib_scroll_left(0x00, 0x05);
    sl_udelay_wait(1000000);
    glib_stop_scroll();
    glib_draw_bmp(&glib_context, oledbw_img);

    glib_scroll_diag_right(0x00, 0x05);
    sl_udelay_wait(1000000);
    glib_stop_scroll();
    glib_draw_bmp(&glib_context, oledbw_img);

    glib_scroll_diag_left(0x00, 0x05);
    sl_udelay_wait(1000000);
    glib_stop_scroll();
  }
}

/***************************************************************************//**
 * Callback on button change.
 *
 * This function overrides a weak implementation defined in the simple_button
 * module. It is triggered when the user activates one of the buttons.
 *
 ******************************************************************************/
void sl_button_on_change(const sl_button_t *handle)
{
  if (sl_button_get_state(handle) == SL_SIMPLE_BUTTON_PRESSED) {
    if (&sl_button_btn0 == handle) {
      if(mode_display == DISPLAY_TEXT)
      {
        mode_display = DISPLAY_PICTURE;
      }
    }
  }
}

/***************************************************************************//**
 * Initialize example.
 ******************************************************************************/
static void oled_app_init(void)
{
  /* Initialize the display */
  glib_init();

  glib_context.backgroundColor = Black;
  glib_context.foregroundColor = White;

  /* Fill lcd with background color */
  glib_clear(&glib_context);

  /* Use Narrow font */
  glib_set_font(&glib_context, (glib_font_t *) &glib_font_6x8);

  glib_draw_string(&glib_context, "Hello World!", 0, 2);

  glib_draw_string(&glib_context, "OLED W Click", 0, 12);

  glib_draw_string(&glib_context, "Press BTN0", 0, 22);

  glib_update_display();
}
